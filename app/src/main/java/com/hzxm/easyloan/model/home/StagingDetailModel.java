public class StagingDetailModel {
    private int id;
    private float periodsMoney;
    private float interestMoney;

    public StagingDetailModel(int id, float periodsMoney, float interestMoney) {
        this.id = id;
        this.periodsMoney = periodsMoney;
        this.interestMoney = interestMoney;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setInterestMoney(float interestMoney) {
        this.interestMoney = interestMoney;
    }

    public void setPeriodsMoney(float periodsMoney) {
        this.periodsMoney = periodsMoney;
    }

    public float getPeriodsMoney() {
        return periodsMoney;
    }

    public float getInterestMoney() {
        return interestMoney;
    }
}
